    prompt_with_default "$@"
    persist_state
  }

  ask_required() {
    prompt_required "$@"
    persist_state
  }

  ask_required_with_default() {
    prompt_required_with_default "$@"
    persist_state
  }

  ask_port() {
    prompt_port "$@"
    persist_state
  }

  ask_choice() {
    prompt_choice "$@"
    persist_state
