      SUBMODEL_REGISTRY_IMAGE_REPO SUBMODEL_REGISTRY_IMAGE_TAG SUBMODEL_REGISTRY_HOST_PORT \
      AAS_DISCOVERY_IMAGE_REPO AAS_DISCOVERY_IMAGE_TAG AAS_DISCOVERY_HOST_PORT \
      RUN_CONFIG_CHECK RUN_STACK_START
  }

