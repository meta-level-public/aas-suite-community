  local SMTP_PORT="${SMTP_PORT-}"
  local SMTP_USE_SSL="${SMTP_USE_SSL-}"
  local SMTP_USE_TLS="${SMTP_USE_TLS-}"
  local SMTP_NEEDS_AUTHENTICATION="${SMTP_NEEDS_AUTHENTICATION-}"
