    ask_required_with_default KEYCLOAK_IMAGE_REPO "Keycloak Image Repository" "$(state_default KEYCLOAK_IMAGE_REPO "quay.io/keycloak/keycloak")"
    ask_required_with_default KEYCLOAK_IMAGE_TAG "Keycloak Image Tag" "$(state_default KEYCLOAK_IMAGE_TAG "26.0")"
