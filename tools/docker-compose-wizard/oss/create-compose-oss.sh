  local FRONTEND_IMAGE_TAG_DEFAULT="latest"
  if [ "$IMAGE_SOURCE" = "local" ]; then
    DESIGNER_BACKEND_IMAGE_REPO_DEFAULT="aas-suite/aas-designer-backend-community"
    DESIGNER_BACKEND_IMAGE_TAG_DEFAULT="local"
    GATEWAY_IMAGE_REPO_DEFAULT="aas-suite/aas-designer-gateway"
    GATEWAY_IMAGE_TAG_DEFAULT="local"
    FRONTEND_IMAGE_REPO_DEFAULT="aas-suite/aas-designer-frontend-community"
    FRONTEND_IMAGE_TAG_DEFAULT="local"
  fi

  ask_required_with_default DESIGNER_BACKEND_IMAGE_REPO "designer-backend Image Repository" "$(state_default DESIGNER_BACKEND_IMAGE_REPO "$DESIGNER_BACKEND_IMAGE_REPO_DEFAULT")"
  ask_required_with_default DESIGNER_BACKEND_IMAGE_TAG "designer-backend Image Tag" "$(state_default DESIGNER_BACKEND_IMAGE_TAG "$DESIGNER_BACKEND_IMAGE_TAG_DEFAULT")"

  ask_required_with_default GATEWAY_IMAGE_REPO "gateway Image Repository" "$(state_default GATEWAY_IMAGE_REPO "$GATEWAY_IMAGE_REPO_DEFAULT")"
  ask_required_with_default GATEWAY_IMAGE_TAG "gateway Image Tag" "$(state_default GATEWAY_IMAGE_TAG "$GATEWAY_IMAGE_TAG_DEFAULT")"
  ask_port GATEWAY_HOST_PORT "gateway Host-Port" "$(state_default GATEWAY_HOST_PORT "8081")"

  ask_required_with_default FRONTEND_IMAGE_REPO "frontend Image Repository" "$(state_default FRONTEND_IMAGE_REPO "$FRONTEND_IMAGE_REPO_DEFAULT")"
  ask_required_with_default FRONTEND_IMAGE_TAG "frontend Image Tag" "$(state_default FRONTEND_IMAGE_TAG "$FRONTEND_IMAGE_TAG_DEFAULT")"
  ask_required_with_default BASE_URL "Gateway / Basis-URL (extern)" "$(state_default BASE_URL "http://localhost:${GATEWAY_HOST_PORT}")"
  migrate_legacy_frontend_base_urls

  component_header "Postgres"
  ask_required_with_default POSTGRES_IMAGE_REPO "postgres Image Repository" "$(state_default POSTGRES_IMAGE_REPO "postgres")"
  ask_required_with_default POSTGRES_IMAGE_TAG "postgres Image Tag" "$(state_default POSTGRES_IMAGE_TAG "16-alpine")"

  local POSTGRES_DB="${POSTGRES_DB-}" POSTGRES_USER="${POSTGRES_USER-}" POSTGRES_PASSWORD="${POSTGRES_PASSWORD-}" POSTGRES_VOLUME="${POSTGRES_VOLUME-}" BASYX_POSTGRES_DB="${BASYX_POSTGRES_DB-}" KEYCLOAK_POSTGRES_DB="${KEYCLOAK_POSTGRES_DB-}"
  ask_required_with_default POSTGRES_DB "Postgres Datenbankname" "$(state_default POSTGRES_DB "aas_designer")"
  ask_required_with_default POSTGRES_USER "Postgres Benutzer" "$(state_default POSTGRES_USER "aas_user")"
  ask_required_with_default POSTGRES_PASSWORD "Postgres Passwort" "$(state_default POSTGRES_PASSWORD "")"
  ask_required_with_default POSTGRES_VOLUME "Postgres Volume-Name" "$(state_default POSTGRES_VOLUME "postgres-data")"
  ask_required_with_default BASYX_POSTGRES_DB "BaSyx Datenbankname (separate DB)" "$(state_default BASYX_POSTGRES_DB "basyx_core")"
  ask_required_with_default KEYCLOAK_POSTGRES_DB "Keycloak Datenbankname (separate DB)" "$(state_default KEYCLOAK_POSTGRES_DB "keycloak")"
  is_db_identifier "$POSTGRES_DB" || die "POSTGRES_DB darf nur Buchstaben, Zahlen und _ enthalten"
  is_db_identifier "$BASYX_POSTGRES_DB" || die "BASYX_POSTGRES_DB darf nur Buchstaben, Zahlen und _ enthalten"
  is_db_identifier "$KEYCLOAK_POSTGRES_DB" || die "KEYCLOAK_POSTGRES_DB darf nur Buchstaben, Zahlen und _ enthalten"

  local KEYCLOAK_MODE="${KEYCLOAK_MODE-}"
  component_header "Keycloak"
  local KEYCLOAK_MODE_DEFAULT
  KEYCLOAK_MODE_DEFAULT="$(state_default KEYCLOAK_MODE "install")"
  ask_choice KEYCLOAK_MODE "Keycloak Modus" "existing,install" "$KEYCLOAK_MODE_DEFAULT"

  local KEYCLOAK_CLUSTER_URL="${KEYCLOAK_CLUSTER_URL-}"
  local KEYCLOAK_ISSUER="${KEYCLOAK_ISSUER-}"
  local KEYCLOAK_WELLKNOWN_URL="${KEYCLOAK_WELLKNOWN_URL-}"
  local KEYCLOAK_PUBLIC_ISSUER="${KEYCLOAK_PUBLIC_ISSUER-}"
  local KEYCLOAK_PUBLIC_WELLKNOWN_URL="${KEYCLOAK_PUBLIC_WELLKNOWN_URL-}"
  local KEYCLOAK_CLIENT_ID="${KEYCLOAK_CLIENT_ID-}"
  local KEYCLOAK_AUDIENCE="${KEYCLOAK_AUDIENCE-}"
  local KEYCLOAK_SCOPES="${KEYCLOAK_SCOPES-}"
  local KEYCLOAK_RESOURCE_ACCESS_NAME="${KEYCLOAK_RESOURCE_ACCESS_NAME-}"
  local KEYCLOAK_ADMIN_REALM="${KEYCLOAK_ADMIN_REALM-}"
  local KEYCLOAK_ADMIN_CLIENT_ID="${KEYCLOAK_ADMIN_CLIENT_ID-}"
  local KEYCLOAK_ADMIN_CLIENT_SECRET="${KEYCLOAK_ADMIN_CLIENT_SECRET-}"
  local KEYCLOAK_ADMIN_USERNAME="${KEYCLOAK_ADMIN_USERNAME-}"
  local KEYCLOAK_ADMIN_PASSWORD="${KEYCLOAK_ADMIN_PASSWORD-}"
  local INITIAL_ORGANISATION_ADMIN_EMAIL="${INITIAL_ORGANISATION_ADMIN_EMAIL-}"
  local INITIAL_ORGANISATION_ADMIN_NAME="${INITIAL_ORGANISATION_ADMIN_NAME-}"
  local INITIAL_ORGANISATION_ADMIN_VORNAME="${INITIAL_ORGANISATION_ADMIN_VORNAME-}"
  local INITIAL_ORGANISATION_ADMIN_PASSWORD="${INITIAL_ORGANISATION_ADMIN_PASSWORD-}"

  local KEYCLOAK_IMAGE_REPO="${KEYCLOAK_IMAGE_REPO-}"
  local KEYCLOAK_IMAGE_TAG="${KEYCLOAK_IMAGE_TAG-}"
  local KEYCLOAK_HOST_PORT="${KEYCLOAK_HOST_PORT-}"
  local KEYCLOAK_REALM_FILE="${KEYCLOAK_REALM_FILE-}"
  local KEYCLOAK_THEMES_PATH="${KEYCLOAK_THEMES_PATH-}"

  if [ "$KEYCLOAK_MODE" = "existing" ]; then
    ask_required_with_default KEYCLOAK_CLUSTER_URL "Keycloak URL aus Gateway-Container (z.B. http://host.docker.internal:8088)" "$(state_default KEYCLOAK_CLUSTER_URL "")"
    ask_required_with_default KEYCLOAK_ISSUER "Keycloak Issuer URL" "$(state_default KEYCLOAK_ISSUER "")"
    ask_required_with_default KEYCLOAK_WELLKNOWN_URL "Keycloak Well-Known URL" "$(state_default KEYCLOAK_WELLKNOWN_URL "")"
