    ask_required_with_default KEYCLOAK_CLIENT_ID "Keycloak Client ID" "$(state_default KEYCLOAK_CLIENT_ID "")"
    ask_required_with_default KEYCLOAK_AUDIENCE "Keycloak Audience" "$(state_default KEYCLOAK_AUDIENCE "$KEYCLOAK_CLIENT_ID")"
    ask_required_with_default KEYCLOAK_SCOPES "Keycloak Scopes" "$(state_default KEYCLOAK_SCOPES "openid profile email roles")"
    ask_required_with_default KEYCLOAK_RESOURCE_ACCESS_NAME "Keycloak Resource Access Name" "$(state_default KEYCLOAK_RESOURCE_ACCESS_NAME "$KEYCLOAK_CLIENT_ID")"
    ask_required_with_default KEYCLOAK_ADMIN_REALM "Keycloak Admin Realm" "$(state_default KEYCLOAK_ADMIN_REALM "master")"
    ask_required_with_default KEYCLOAK_ADMIN_CLIENT_ID "Keycloak Admin Client ID" "$(state_default KEYCLOAK_ADMIN_CLIENT_ID "admin-cli")"
    ask_with_default KEYCLOAK_ADMIN_CLIENT_SECRET "Keycloak Admin Client Secret (optional)" "$(state_default KEYCLOAK_ADMIN_CLIENT_SECRET "")"
    ask_required_with_default KEYCLOAK_ADMIN_USERNAME "Keycloak Admin Benutzer" "$(state_default KEYCLOAK_ADMIN_USERNAME "")"
    ask_required_with_default KEYCLOAK_ADMIN_PASSWORD "Keycloak Admin Passwort" "$(state_default KEYCLOAK_ADMIN_PASSWORD "")"
  else
