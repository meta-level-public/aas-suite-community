  local PROJECT_NAME="${PROJECT_NAME-}"
  local STACK_DIR="${STACK_DIR-}"
  local COMPOSE_FILE
  local ENV_FILE
  local NETWORK_NAME="${NETWORK_NAME-}"
  local IMAGE_SOURCE_PREV="${IMAGE_SOURCE-}"
  local IMAGE_SOURCE="${IMAGE_SOURCE-}"
  local DEFAULT_STACK_DIR

  DEFAULT_STACK_DIR="$(default_generated_stack_dir)"

  component_header "Allgemein"
  ask_required_with_default PROJECT_NAME "Projektname" "$(state_default PROJECT_NAME "aas-suite")"
